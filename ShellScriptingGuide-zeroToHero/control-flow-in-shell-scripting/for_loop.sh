##The `for` loop is used to iterate over a list of items or a range of numbers.

#!/bin/bash

##Example 1: Iterate Over a List of Strings**


for NAME in Fahad Bilal Jahan; do
    echo "Hello, $NAME!"
done


##Example 2: Iterate Over a Range of Numbers**


for i in {1..9}; do
    echo "Iteration $i"
done
